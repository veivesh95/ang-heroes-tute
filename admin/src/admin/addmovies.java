    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        try {
            String movieID = jTextField1.getText();
            String imageID = jTextField2.getText();
            String moviename = jTextField3.getText();
            String price = jTextField4.getText();
            String year = jTextField5.getText();
            String lead = jTextField6.getText();
            String genre = jTextField7.getText();
            String duration = jTextField8.getText();
            String rating = jTextField9.getText();
            String Addedby = jTextField10.getText();
            String total = jTextField11.getText();
            String available = jTextField13.getText();

            Connection c = DB.getConnect();
            String query;
            query = "insert into movie (movieID,imgId,name,price,year,lead,genre,duration,rating,addedBy,total,available,image) values (?,?,?,?,?,?,?,?,?,?,?,?,?)";

            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField2.getText().isEmpty()
                    || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty()
                    || jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty() || jTextField8.getText().isEmpty()
                    || jTextField9.getText().isEmpty() || jTextField10.getText().isEmpty() || jTextField11.getText().isEmpty()
                    || jTextField12.getText().isEmpty() || jTextField13.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "One of the required field is empty!", "Error", JOptionPane.ERROR_MESSAGE);
               
                
            } else {

                PreparedStatement statement = c.prepareStatement(query);
                statement.setString(1, movieID);
                statement.setString(2, imageID);
                statement.setString(3, moviename);
                statement.setString(4, price);
                statement.setString(5, year);
                statement.setString(6, lead);
                statement.setString(7, genre);
                statement.setString(8, duration);
                statement.setString(9, rating);
                statement.setString(10, Addedby);
                statement.setString(11, total);
                statement.setString(12, available);
                statement.setBytes(13, photo);

                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "New movie has been added to the system");

                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_AddActionPerformed

