    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filepath = f.getAbsolutePath();
        jTextField12.setText(filepath);

        ImageIcon img = new ImageIcon(f.getAbsolutePath());
        Rectangle rectangle_img = jLabel12.getBounds();
        Image scaledImage = img.getImage().getScaledInstance(rectangle_img.width, rectangle_img.height, Image.SCALE_DEFAULT);
        img = new ImageIcon(scaledImage);
        jLabel12.setIcon(img);

        try {
            File photo = new File(filepath);
            FileInputStream fis = new FileInputStream(photo);
            ByteArrayOutputStream byte_photo = new ByteArrayOutputStream();
            byte[] buffer = new byte[1024];
            for (int readnumber; (readnumber = fis.read(buffer)) != -1;) {
                byte_photo.write(buffer, 0, readnumber);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        photo = byte_photo.toByteArray();
    }//GEN-LAST:event_SearchActionPerformed

